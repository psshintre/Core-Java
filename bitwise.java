package basicprogram;

public class bitwise {
public static void main(String[] args) {
		
		int a = 20;
		int b = 10;
		
		System.out.println("a & b ="+(a & b));
		System.out.println("a | b ="+(a | b));
		System.out.println("a ^ b ="+(a ^ b));
		System.out.println("~ a="+ ~a);
		
		System.out.println((5 > 3) && (8 > 5)); 
		System.out.println((5 > 3) || (8 > 5)); 
				
	}
}


